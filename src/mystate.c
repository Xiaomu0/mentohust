static void setTimer(unsigned interval) /* 设置定时器 */
{
	struct itimerval timer;
	timer.it_value.tv_sec = interval;
	timer.it_value.tv_usec = 0;
	timer.it_interval.tv_sec = interval;
	timer.it_interval.tv_usec = 0;
	setitimer(ITIMER_REAL, &timer, NULL);
}

void customizeServiceName(char* service)
{
	if (strncmp(service, "internet", 8) != 0) {
		int serviceNameLen = strnlen(service, 128);

		pkt_start = (unsigned char*)malloc(sizeof(pkt1));
		pkt_identity = (unsigned char*)malloc(sizeof(pkt2));
		pkt_md5 = (unsigned char*)malloc(sizeof(pkt3));

		memmove(pkt_start, pkt1, sizeof(pkt1));
		memmove(pkt_identity, pkt2, sizeof(pkt2));
		memmove(pkt_md5, pkt3, sizeof(pkt3));

		memset(pkt_start + 344, 0, 8);
		memset(pkt_identity + 344, 0, 8);
		memset(pkt_md5 + 360, 0, 8);

		memmove(pkt_start + 344, service, serviceNameLen);
		memmove(pkt_identity + 344, service, serviceNameLen);
		memmove(pkt_md5 + 360, service, serviceNameLen);
	} else {
		pkt_start = pkt1;
		pkt_identity = pkt2;
		pkt_md5 = pkt3;
	}
}

int switchState(int type)
{
	if (state == type) /* 跟上次是同一状态？ */
		sendCount++;
	else
	{
		state = type;
		sendCount = 0;
	}
	if (sendCount>=MAX_SEND_COUNT && type!=ID_ECHO)  /* 超时太多次？ */
	{
		switch (type)
		{
		case ID_START:
			printf(_(">> 找不到服务器，重启认证!\n"));
			break;
		case ID_IDENTITY:
			printf(_(">> 发送用户名超时，重启认证!\n"));
			break;
		case ID_CHALLENGE:
			printf(_(">> 发送密码超时，重启认证!\n"));
			break;
		case ID_WAITECHO:
			printf(_(">> 等候响应包超时，自行响应!\n"));
			return switchState(ID_ECHO);
		}
		return restart();
	}
	switch (type)
	{
	case ID_DHCP:
		return renewIP();
	case ID_START:
		return sendStartPacket();
	case ID_IDENTITY:
		return sendIdentityPacket();
	case ID_CHALLENGE:
		return sendChallengePacket();
	case ID_WAITECHO:	/* 塞尔的就不ping了，不好计时 */
		return waitEchoPacket();
	case ID_ECHO:
		if (pingHost && sendCount*echoInterval > 60) {	/* 1分钟左右 */
			if (isOnline() == -1) {
				printf(_(">> 认证掉线，开始重连!\n"));
				return switchState(ID_START);
			}
			sendCount = 1;
		}
#ifndef NO_ARP
		if (gateMAC[0] != 0xFE)
			sendArpPacket();
#endif
		return sendEchoPacket();
	case ID_DISCONNECT:
		return sendLogoffPacket();
	}
	return 0;
}

int restart()
{
	if (startMode >= 3)	/* 标记服务器地址为未获取 */
		startMode -= 3;
	state = ID_START;
	sendCount = -1;
	setTimer(restartWait);	/* restartWait秒后或者服务器请求后重启认证 */
	return 0;
}

static int renewIP()
{
	setTimer(0);	/* 取消定时器 */
	printf(_(">> 正在获取IP...\n"));
    setreuid(0,0);
    printf("%s\n", dhcpScript);
	system(dhcpScript);
    printf(_(">> 操作结束。\n"));
	dhcpMode += 3; /* 标记为已获取，123变为456，5不需再认证*/
	if (fillHeader() == -1)
		exit(EXIT_FAILURE);
	if (dhcpMode == 5)
		return switchState(ID_ECHO);
	return switchState(ID_START);
}

static void fillEtherAddr(u_int32_t protocol)
{
	memset(sendPacket, 0, 0x3E8);
	memcpy(sendPacket, destMAC, 6);
	memcpy(sendPacket+0x06, localMAC, 6);
	*(u_int32_t *)(sendPacket+0x0C) = htonl(protocol);
}

static int sendStartPacket()
{
	if (startMode%3 == 2)	/* 赛尔 */
	{
		if (sendCount == 0)
		{
			printf(_(">> 寻找服务器...\n"));
			memcpy(sendPacket, STANDARD_ADDR, 6);
			memcpy(sendPacket+0x06, localMAC, 6);
			*(u_int32_t *)(sendPacket+0x0C) = htonl(0x888E0101);
			*(u_int16_t *)(sendPacket+0x10) = 0;
			memset(sendPacket+0x12, 0xa5, 42);
			setTimer(timeout);
		}
		return pcap_sendpacket(hPcap, sendPacket, 60);
	}
	if (sendCount == 0)
	{
		printf(_(">> 寻找服务器...\n"));
		//fillStartPacket();
		fillEtherAddr(0x888E0101);
		memcpy(sendPacket + 0x12, pkt_start, sizeof(pkt1));
		memcpy(sendPacket + 0xe2, computeV4(pad, 16), 0x80);
		setTimer(timeout);
	}
	return pcap_sendpacket(hPcap, sendPacket, 524);
}

